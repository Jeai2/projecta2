/** 지지 → 음력 월 번호 (1–12) */
export function jiToMonthNumber(ji: Ji): number {
  const i = JI_ORDER.indexOf(ji);
  return i >= 0 ? i + 1 : 1;
}

/** 년지 포함 음력 생월만큼 이동. 남자=정방향, 여자=역방향 (년지=1월이므로 steps = lunarMonth - 1) */
export function moveJiByMonth(
  startJi: Ji,
  lunarMonth: number,
  direction: "forward" | "backward"
): Ji {
  const idx = JI_ORDER.indexOf(startJi);
  if (idx < 0) return startJi;
  const steps = Math.max(0, (lunarMonth - 1) % 12);
  if (steps === 0) return startJi;
  const len = JI_ORDER.length;
  const nextIdx =
    direction === "forward"
      ? (idx + steps) % len
      : (idx - steps + len * 10) % len;
  return JI_ORDER[nextIdx];
}

