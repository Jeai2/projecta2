/** 역방향 천간합 키 → 정방향 정규화 (예: "己甲" → "甲己") */
const CANONICAL_HAP_PAIRS = new Set(["甲己", "乙庚", "丙辛", "丁壬", "戊癸"]);

export function normalizeCheonganhapKey(key: string): string {
  if (CANONICAL_HAP_PAIRS.has(key)) return key;
  const reversed = key[1] + key[0];
  return CANONICAL_HAP_PAIRS.has(reversed) ? reversed : key;
}

