/**
 * 지지(地支) 간의 상호작용(합, 충, 형, 해, 파)의 특성과 잠재력을 정의하는 인터페이스
 */
export interface InteractionDefinition {
  name: string; // 상호작용의 이름 (예: "자오충 (子午沖)")
  type: "삼합" | "방합" | "육합" | "충" | "형" | "파" | "해"; // 상호작용의 종류
  characters: string[]; // 관계를 맺는 글자들 (예: ["자", "오"])
  keywords: string[]; // 해당 상호작용을 대표하는 핵심 키워드
  summary: string; // 상호작용에 대한 한 줄 요약
  description: string; // 해당 상호작용의 본질에 대한 상세 설명
  effect: {
    // 사주에 있을 때 나타날 수 있는 잠재적 영향
    positive: string; // 긍정적으로 발현될 때의 모습
    negative: string; // 부정적으로 발현될 때 또는 주의할 점
  };
}

